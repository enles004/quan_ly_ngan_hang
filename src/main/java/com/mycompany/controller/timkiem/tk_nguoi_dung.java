    public void performSearch() {
        try {
            Connection con = db.connect();
            Statement st = con.createStatement();
            
            String ho = txtHo.getText().trim();
            String ten = txtTen.getText().trim();
            String ngaySinh = txtNgaySinh.getText().trim();
            String gioiTinh = cb.getSelectedItem().toString();
            String diaChi = txtDiaChi.getText().trim();
            String soDienThoai = txtSoDienThoai.getText().trim();
            String email = txtEmail.getText().trim();
            String soCongDan = txtSoCongDan.getText().trim();

            StringBuilder sql = new StringBuilder("SELECT * FROM nguoi_dung WHERE 1=1");

            // Thêm điều kiện vào truy vấn nếu trường không rỗng
            if (!ho.isEmpty()) {
                sql.append(" AND ho LIKE N'%").append(ho).append("%'");
            }
            if (!ten.isEmpty()) {
                sql.append(" AND ten LIKE N'%").append(ten).append("%'");
            }
            if (!ngaySinh.isEmpty()) {
                sql.append(" AND ngay_sinh = '").append(ngaySinh).append("'");
            }
            if (!gioiTinh.isEmpty()) {
                sql.append(" AND gioi_tinh LIKE N'%").append(gioiTinh).append("%'");
            }
            if (!diaChi.isEmpty()) {
                sql.append(" AND dia_chi LIKE N'%").append(diaChi).append("%'");
            }
            if (!soDienThoai.isEmpty()) {
                sql.append(" AND so_dien_thoai LIKE '%").append(soDienThoai).append("%'");
            }
            if (!email.isEmpty()) {
                sql.append(" AND email LIKE '%").append(email).append("%'");
            }
            if (!soCongDan.isEmpty()) {
                sql.append(" AND so_cong_dan LIKE '%").append(soCongDan).append("%'");
            }

            ResultSet rs = st.executeQuery(sql.toString());

            // Thiết lập bảng hiển thị kết quả
            String[] columnNames = {"ID", "Họ", "Tên", "Ngày sinh", "Giới tính", "Địa chỉ", "Số điện thoại", "Email", "Số công dân"};
            DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);

            while (rs.next()) {
                Vector<Object> row = new Vector<>();
                row.add(rs.getInt("id"));
                row.add(rs.getString("ho"));
                row.add(rs.getString("ten"));
                row.add(rs.getDate("ngay_sinh"));
                row.add(rs.getString("gioi_tinh"));
                row.add(rs.getString("dia_chi"));
                row.add(rs.getString("so_dien_thoai"));
                row.add(rs.getString("email"));
                row.add(rs.getString("so_cong_dan"));
                tableModel.addRow(row);
            }
            
            tbNguoiDung.setModel(tableModel);

            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

