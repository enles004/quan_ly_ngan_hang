    public static String formatNumber(String input) {
        try {
            long number = Long.parseLong(input.replaceAll(",", ""));
            DecimalFormat decimalFormat = new DecimalFormat("#,###");
            return decimalFormat.format(number);
        } catch (NumberFormatException ex) {
            return input;
        }
    }
    private void load_cb(){
        try {
            con = db.connect();
            Statement st = con.createStatement();
            String sql = "select * from cb_uu_dai";
            ResultSet rs = st.executeQuery(sql);
            txtUUdai.removeAllItems();
            txtUUdai.addItem("-Chọn ưu đãi-");
            while(rs.next()){
                txtUUdai.addItem(rs.getString("ten_uu_dai"));
            }
        } catch (Exception e) {
        }
    }
    public void performSearch() {
        DefaultTableModel tb = new DefaultTableModel(new String[]{"ID", "Tên thẻ", "Ưu đãi", "Hạn mức sử dụng", "Phí dịch vụ"}, 0);
        try {
            con = db.connect();
            Statement st = con.createStatement();

            String tenLoaiThe = txtTenLoaiThe.getText().trim();
            String uuDai = txtUUdai.getSelectedItem().toString();
            String hanMucMin = txtHanMucMin.getText().trim().replaceAll(",", "");
            String hanMucMax = txtHanMucMax.getText().trim().replaceAll(",", "");
            String phiMin = txtPhiMin.getText().trim().replaceAll(",", "");
            String phiMax = txtPhiMax.getText().trim().replaceAll(",", "");

            StringBuilder sql = new StringBuilder("SELECT * FROM loai_the WHERE 1=1");

            if (!tenLoaiThe.isEmpty()) {
                sql.append(" AND ten_loai_the LIKE N'%").append(tenLoaiThe).append("%'");
            }
            if (!uuDai.isEmpty()) {
                sql.append(" AND uu_dai LIKE N'%").append(uuDai).append("%'");
            }
            if (!hanMucMin.isEmpty()) {
                sql.append(" AND han_muc_su_dung >= '").append(hanMucMin).append("'");
            }
            if (!hanMucMax.isEmpty()) {
                sql.append(" AND han_muc_su_dung <= '").append(hanMucMax).append("'");
            }
            if (!phiMin.isEmpty()) {
                sql.append(" AND phi_dich_vu >= '").append(phiMin).append("'");
            }
            if (!phiMax.isEmpty()) {
                sql.append(" AND phi_dich_vu <= '").append(phiMax).append("'");
            }

            ResultSet rs = st.executeQuery(sql.toString());

            while (rs.next()) {
                Vector<String> v = new Vector<>();
                v.add(rs.getString("id"));
                v.add(rs.getString("ten_loai_the"));
                v.add(rs.getString("uu_dai"));
                v.add(rs.getString("han_muc_su_dung"));
                v.add(rs.getString("phi_dich_vu"));
                tb.addRow(v);
            }
            tb_lt.setModel(tb);
            rs.close();
            st.close();
            con.close();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
