    private void load(){
        try {
            con = db.connect();
            Statement st = con.createStatement();
            String tim = txt_tk.getText().trim();
            String sql = "select * "
            + "from giao_dich "
            + "where trang_thai = 'thanh_cong'";
            ResultSet rs = st.executeQuery(sql);
            String[] td = {"Loại giao dịch", "Tên người gửi", "SĐT người gửi", "STK người gửi", "STK người nhận", "Số tiền", "Nội dung", "Ngày GD"};
            DefaultTableModel tb = new DefaultTableModel(td, 0){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; 
                }
            };
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("loai_giao_dich"));
                v.add(rs.getString("ten_nguoi_gui"));
                v.add(rs.getString("so_dien_thoai_nguoi_gui"));
                v.add(rs.getString("so_tai_khoan_nguoi_gui"));
                v.add(rs.getString("so_tai_khoan_nguoi_nhan"));
                v.add(rs.getString("so_tien"));
                v.add(rs.getString("mo_ta"));
                v.add(rs.getString("ngay_giao_dich"));
                tb.addRow(v);
            }
            tb_nd.setModel(tb);
            con.close();
        } catch (Exception e){
            e.printStackTrace();
        }
    }
